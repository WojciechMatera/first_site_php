Polityka AS Apartments dotycząca Ciasteczek<br/><br/>
 Niniejsza Polityka dotycząca Ciasteczek odnosi się do strony www.strzel.com.pl dalej zwaną „stroną”<br/><br/>

Przez używanie strony wyrażasz zgodę na używanie ciasteczek zgodnie z tą Polityką Ciasteczek. Jeżeli nie zgadzasz się na używanie<br/> przez nas ciasteczek, powinieneś zmienić ustawienia swojej przeglądarki w odpowiedni sposób lub zrezygnować z używania strony.
 <br/>
Co to są ciasteczka?<br/>
 Ciasteczka (ang. cookies) to niewielkie pliki, zapisywane i przechowywane na twoim komputerze, tablecie lub smartphonie podczas<br/> gdy odwiedzasz różne strony w internecie. Ciasteczko zazwyczaj zawiera nazwę strony internetowej, z której pochodzi, „długość<br/> życia” ciasteczka (to znaczy czas jego istnienia), oraz przypadkowo wygenerowany unikalny numer służący do identyfikacji<br/> przeglądarki, z jakiej następuje połączenie ze stroną internetową.<br/><br/>
  
Do czego używamy ciasteczek?<br/>
AS Apartments używa ciasteczek w różnych celach:<br/><br/>

    by strona działała szybciej i była łatwiejsza w użyciu,<br/>
    do zbierania anonimowych, zagregowanych statystyk, które pozwalają nam zrozumieć jak ludzie używają naszej strony i pomagają w<br/> poprawianiu jej funkcjonalności i zawartości.<br/>
    do realizowania usługi rezerwacji on-line i ewentualnej płatności za rezerwację.<br/><br/>

Używając ciasteczek w wyżej opisany sposób nigdy nie identyfikujemy tożsamości użytkowników na podstawie informacji przechowywanych<br/> w ciasteczkach.<br/><br/>
 
Jak długo przechowywane są dane w ciasteczkach?<br/>
 Na stronie mogą być używane dwa rodzaje ciasteczek – sesyjne oraz stałe. Te pierwsze pozostają na Twoim urządzeniu jedynie podczas<br/> korzystania ze strony. Ciasteczka stałe pozostają na Twoim urządzeniu tak długo jak długo mają ustawiony czas życia lub do<br/> momentu, kiedy je usuniesz.<br/><br/>
 
Rodzaje ciasteczek używanych na stronie<br/><br/>
 
Typ Ciasteczka   	Do czego są wykorzystywane?<br/>
Konieczne do działania stron 	Niezbędne do prawidłowego funkcjonowania strony, pozwalają Ci na poruszanie się po nich oraz<br/> używanie ich elementów. Przykładowo mogą zapamiętywać poprzednie czynności (np. wpisane teksty) podczas wracania na stronę w tej<br/> samej sesji.<br/>
Poprawiające wydajność 	Zbieranie informacji o tym jak odwiedzający korzystają ze strony poprzez dostarczanie informacji na temat<br/> obszarów które odwiedzają, czasu jaki na nich spędzają oraz problemów jakie na nich napotykają, jak np. komunikaty o błędach. To<br/> pozwala nam poprawiać działanie strony<br/>
Poprawiające funkcjonalność 	Zapamiętywanie Twoich ustawień i wyborów (np. nazwa użytkownika, region, w którym jesteś,<br/> personalizowane ustawienia treści) by dostarczyć Ci bardziej spersonalizowane treści i usługi. Pozwalają Ci również na oglądanie<br/> filmów i używanie narzędzi społecznościowych, np. blogów, chatów i forów.<br/>
Reklamowe 	Dostarczanie Ci reklam odpowiadających Twoim zainteresowaniom oraz ograniczanie liczby wyświetleń danej reklamy. Te<br/> ciasteczka pozwalają nam także mierzyć efektywność kampanii reklamowych.<br/><br/>
  
Czy używamy ciasteczek podmiotów trzecich?<br/>
Tak, korzystając ze strony możesz otrzymywać ciasteczka pochodzące od współpracujących podmiotów trzecich takich jak np. Facebook i<br/> Gogle. Więcej informacji na temat tych ciasteczek możesz znaleźć na stronach internetowych poszczególnych podmiotów trzecich.<br/><br/>
 
W jaki sposób mogę zmienić ustawienia dot. ciasteczek albo je usunąć?<br/>
Większość przeglądarek internetowych jest początkowo ustawionych na automatyczne przyjmowanie ciasteczek. Możesz jednak zmienić<br/> ustawienia przeglądarki tak, aby ciasteczka były blokowane – w całości lub w jakiejś części, np. tylko od stron trzecich, albo,<br/> aby każdorazowo otrzymywać komunikat w momencie, kiedy ciasteczka są wysyłane na Twoje urządzenie. Pamiętaj jednak, że jeżeli<br/> zablokujesz używane przez nas ciasteczka, może to negatywnie wpłynąć na wygodę korzystania ze strony, na przykład możesz nie być<br/> w stanie dokonać rezerwacji lub płatności. Uniemożliwisz nam także zbieranie anonimowych informacji nt. używania naszej<br/> strony w celu stałego poprawiania zawartości strony i usług AS Apartments.<br/><br/>


